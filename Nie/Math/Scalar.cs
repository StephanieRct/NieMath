namespace Nie.Math
{
    public struct Scalar
    {
        public static float zero { get { return 0; } }
        public static float half { get { return 0.5f; } }
        public static float one { get { return 1; } }
        public static float two { get { return 2; } }
        public static float minusHalf { get { return -0.5f; } }
        public static float minusOne { get { return -1; } }
        public static float minusTwo { get { return -2; } }

        public static float piQuater { get { return 0.78539816339744830961566084581988f; } }
        public static float piHalf { get { return 1.5707963267948966192313216916398f; } }
        public static float piThreeQuater { get { return 2.3561944901923449288469825374596f; } }
        public static float pi { get { return 3.1415926535897932384626433832795f; } }
        public static float tau { get { return 6.283185307179586476925286766559f; } }

        
        public static float rad2deg { get { return 57.295779513082320876798154814105f; } }
        public static float deg2rad { get { return 0.01745329251994329576923690768489f; } }
        
    }
}